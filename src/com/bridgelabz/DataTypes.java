package com.bridgelabz;

public class DataTypes {
    static int a;
    static long c;
    static short d;
    static byte e;
    static boolean f;
    static char g;
    static float h;
    static double i;
    public static void main(String[] args) {
        System.out.println("value of int: " + a);
        // System.out.println("value of decimal: " + b);
        System.out.println("value of long: " + c);
        System.out.println("value of short: " + d);
        System.out.println("value of byte: " + e);
        System.out.println("value of boolean: " + f);
        System.out.println("value of char: " + g);
        System.out.println("value of float: " + h);
        System.out.println("value of double: " + i);
    }
}
